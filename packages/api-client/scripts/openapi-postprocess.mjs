import { execSync } from 'node:child_process';
import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const apiServicesFolder = path.resolve(__dirname, '../src/data-access/api/openapi');
const srcModelsDir = path.resolve(apiServicesFolder, 'models');
const commonDir = path.resolve(__dirname, '../src/common');
const destModelsDir = path.resolve(commonDir, 'models');
const indexFilePath = path.join(apiServicesFolder, 'index.ts');
const commonIndexPath = path.join(commonDir, 'index.ts');

/**
 * Move the models directory from the OpenAPI output to src/common/models.
 */
function moveModels() {
  if (!fs.existsSync(srcModelsDir)) {
    console.warn(`⚠️  Models directory not found at ${srcModelsDir}. Skipping move step.`);
    return;
  }

  if (!fs.existsSync(commonDir)) {
    fs.mkdirSync(commonDir, { recursive: true });
    console.log('📂  Created ./src/common');
  }

  if (fs.existsSync(destModelsDir)) {
    fs.rmSync(destModelsDir, { recursive: true, force: true });
  }

  fs.renameSync(srcModelsDir, destModelsDir);
  console.log(`🚚  Moved models to ${destModelsDir}`);
}

/**
 * Fix import paths in generated service files so they reference
 * the models from their new location in src/common/models.
 */
function fixImports() {
  const servicesDir = path.resolve(apiServicesFolder, 'services');
  if (!fs.existsSync(servicesDir)) {
    console.warn('⚠️  Services directory not found. Skipping import fixes.');
    return;
  }

  const filenames = fs.readdirSync(servicesDir);
  for (const filename of filenames) {
    const fullPath = path.join(servicesDir, filename);
    if (!fs.lstatSync(fullPath).isFile()) continue;

    const src = fs.readFileSync(fullPath, 'utf8');
    const dst = src.replace(/(['"])(?:\.\.\/)+models\//g, '$1../../../../common/models/');

    if (dst !== src) {
      fs.writeFileSync(fullPath, dst);
      console.log(`🔧  Fixed imports in ${filename}`);
    }
  }
}

/**
 * Generate a barrel index file at src/common/index.ts that
 * re-exports every model type.
 */
function generateCommonIndex() {
  if (!fs.existsSync(destModelsDir)) {
    console.warn('⚠️  No models found to index. Skipping common index generation.');
    return;
  }

  const modelFiles = fs.readdirSync(destModelsDir).filter((f) => f.endsWith('.ts'));

  const exports = modelFiles
    .map((f) => path.basename(f, '.ts'))
    .sort()
    .map((name) => `export type { ${name} } from "./models/${name}";`)
    .join('\n');

  const banner = '/* Auto-generated by openapi-postprocess -- do not edit */';
  fs.writeFileSync(commonIndexPath, `${banner}\n${exports}\n`);
  console.log(`📄  Created ${path.relative(process.cwd(), commonIndexPath)}`);
}

/**
 * Remove model re-exports from the OpenAPI index.ts so consumers
 * import models from src/common instead.
 */
function cleanIndexExports() {
  if (!fs.existsSync(indexFilePath)) {
    console.warn(`⚠️  index.ts not found at ${indexFilePath}. Skipping export cleanup.`);
    return;
  }

  const lines = fs.readFileSync(indexFilePath, 'utf8').split(/\r?\n/);
  const filtered = lines.filter(
    (l) => !/^export\s+(?:type\s+)?\{[^}]*\}\s+from\s+['"]\.\/models\//.test(l.trim()),
  );

  if (filtered.length !== lines.length) {
    fs.writeFileSync(indexFilePath, filtered.join('\n'));
    console.log('🧹  Removed model exports from index.ts');
  }
}

/**
 * Generate a barrel index at services/index.ts re-exporting all service classes.
 */
function generateServicesIndex() {
  const servicesDir = path.resolve(apiServicesFolder, 'services');
  if (!fs.existsSync(servicesDir)) return;

  const serviceFiles = fs
    .readdirSync(servicesDir)
    .filter((f) => f.endsWith('.ts') && f !== 'index.ts');
  const exports = serviceFiles
    .map((f) => path.basename(f, '.ts'))
    .sort()
    .map((name) => `export { ${name} } from './${name}';`)
    .join('\n');

  const banner = '/* Auto-generated by openapi-postprocess -- do not edit */';
  fs.writeFileSync(path.join(servicesDir, 'index.ts'), `${banner}\n${exports}\n`);
  console.log('📄  Created services/index.ts');
}

/**
 * Generate the root src/index.ts barrel that re-exports core, services, and models.
 */
function generateRootIndex() {
  const rootIndexPath = path.resolve(__dirname, '../src/index.ts');

  const servicesDir = path.resolve(apiServicesFolder, 'services');
  const serviceFiles = fs.existsSync(servicesDir)
    ? fs.readdirSync(servicesDir).filter((f) => f.endsWith('.ts') && f !== 'index.ts')
    : [];

  const modelFiles = fs.existsSync(destModelsDir)
    ? fs.readdirSync(destModelsDir).filter((f) => f.endsWith('.ts'))
    : [];

  const lines = [
    '/* Auto-generated by openapi-postprocess -- do not edit */',
    '',
    '// Core',
    "export { ApiError } from './data-access/api/openapi/core/ApiError';",
    "export { CancelablePromise, CancelError } from './data-access/api/openapi/core/CancelablePromise';",
    "export { OpenAPI } from './data-access/api/openapi/core/OpenAPI';",
    "export type { OpenAPIConfig } from './data-access/api/openapi/core/OpenAPI';",
    '',
    '// Services',
    ...serviceFiles.map((f) => {
      const name = path.basename(f, '.ts');
      return `export { ${name} } from './data-access/api/openapi/services/${name}';`;
    }),
    '',
    '// Models',
    ...modelFiles.map((f) => {
      const name = path.basename(f, '.ts');
      return `export type { ${name} } from './common/models/${name}';`;
    }),
    '',
  ];

  fs.writeFileSync(rootIndexPath, lines.join('\n'));
  console.log(`📄  Created ${path.relative(process.cwd(), rootIndexPath)}`);
}

/**
 * Format all generated and moved files with Biome.
 */
function formatFiles() {
  try {
    const rootIndex = path.resolve(__dirname, '../src/index.ts');
    execSync(`npx biome format --write "${apiServicesFolder}" "${commonDir}" "${rootIndex}"`, {
      stdio: 'inherit',
    });
  } catch (e) {
    console.warn('Formatting skipped (biome not available):', e.message);
  }
}

function main() {
  moveModels();
  fixImports();
  generateCommonIndex();
  cleanIndexExports();
  generateServicesIndex();
  generateRootIndex();
  formatFiles();
  console.log('✨  OpenAPI Post-Process Done');
}

main();
